public class Printing
{
	public static void main(String[] args)
	{
		System.out.println('a' + 2);
		System.out.println('a' + '2');
		System.out.println((char)('a' + 2));
		System.out.println("ppj" + 'a' + '2');
		System.out.println("ppj" + 'a' + '\t' + '2');
		System.out.println('a' + '2' + "ppj");
	}
}